export type DictKey = keyof typeof DICT

export function t(key: DictKey, lang: Lang): string {
  const entry = DICT[key]
  return entry[lang] ?? entry.en
}

// Convenience helpers for status / zone / priority
export function tStatus(status: string, lang: Lang): string {
  const k = `status_${status}` as DictKey
  return DICT[k] ? t(k, lang) : status.replace(/_/g, ' ')
}
export function tZone(zone: string | null | undefined, lang: Lang): string {
  if (!zone) return '—'
  const k = `zone_${zone}` as DictKey
  return DICT[k] ? t(k, lang) : zone
}
export function tPriority(priority: string, lang: Lang): string {
  const k = `priority_${priority}` as DictKey
  return DICT[k] ? t(k, lang) : priority
}
export function tPackage(packageType: string | null | undefined, lang: Lang): string {
  if (!packageType) return '—'
  const k = `package_${packageType}` as DictKey
  return DICT[k] ? t(k, lang) : packageType
}
